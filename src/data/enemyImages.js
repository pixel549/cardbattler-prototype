/**
 * Returns the resolved image URL for an enemy definition ID, or null if none.
 * @param {string} enemyDefId - e.g. "E_HACKER_DRONE"
 * @returns {string|null}
 */
export function getEnemyImage(enemyDefId) {
  const filename = ENEMY_IMAGE_MAP[enemyDefId];
  if (!filename) return null;
  return imageByFilename[filename] ?? null;
}
